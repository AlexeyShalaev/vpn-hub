"""Каталог тарифных планов провайдеров (справочные данные).

При создании сервера панель предлагает выбрать существующий план провайдера, чтобы сразу подтянуть
цену (финансовый учёт) и сетевую квоту трафика (лимит байт сервера). Данные курированы по офиц.
сайтам провайдеров (FirstByte: firstbyte.ru/vps-vds/*). Это СПРАВОЧНИК, не источник правды по цене —
владелец может скорректировать значения. Расширяется добавлением ключа-провайдера в PLANS.

Поля плана: id, name, region, cpu, ramGb, diskGb, diskType, portMbps, trafficTb (None=безлимит),
price, currency, period. bandwidth-квота = trafficTb × 1024⁴ байт (в тех же единицах, что и UI ГБ).
"""

from __future__ import annotations

from typing import Any

TIB = 1024**4  # 1 ТБ (бинарно, как в UI: ГБ = 1024³)


def _p(
    pid: str, name: str, region: str, cpu: int, ram: float, disk: int, port: int, tb: float | None, price: int
) -> dict:
    return {
        "id": pid,
        "name": name,
        "region": region,
        "cpu": cpu,
        "ramGb": ram,
        "diskGb": disk,
        "diskType": "SSD",
        "portMbps": port,
        "trafficTb": tb,
        "price": price,
        "currency": "RUB",
        "period": "month",
    }


