PLANS: dict[str, list[dict]] = {
    "firstbyte": _FIRSTBYTE,
}


def plans_for(provider_id: str) -> list[dict[str, Any]]:
    """Планы провайдера по его id (пустой список, если каталога нет)."""
    return PLANS.get((provider_id or "").strip().lower(), [])


def plan_bandwidth_bytes(plan: dict) -> int | None:
    """Квота трафика плана в байтах (None = безлимит)."""
    tb = plan.get("trafficTb")
    return int(tb * TIB) if tb else None
