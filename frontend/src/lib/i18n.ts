const DICTS: Record<Lang, Dict> = { ru, en };

// ── определение языка ───────────────────────────────────────────────────────────
export function detectLang(): Lang {
  const saved = typeof localStorage !== "undefined" ? localStorage.getItem("vpnhub.lang") : null;
  if (saved === "ru" || saved === "en") return saved;
  const nav = typeof navigator !== "undefined" ? navigator.language : "";
  return nav?.toLowerCase().startsWith("en") ? "en" : "ru";
}

// ── интерполяция {var} ──────────────────────────────────────────────────────────
function interpolate(s: string, vars?: Record<string, string | number>): string {
  if (!vars) return s;
  return s.replace(/\{(\w+)\}/g, (m, k) => (k in vars ? String(vars[k]) : m));
}

// ── плюрализация ────────────────────────────────────────────────────────────────
// ru — по правилам русского склонения (та же логика, что была в экранах);
// en — простое n === 1 ? one : five (форма «other»).
function pickPlural(lang: Lang, n: number, forms: PluralForms): string {
  if (lang === "en") return n === 1 ? forms.one : forms.five;
  const n10 = n % 10;
  const n100 = n % 100;
  if (n10 === 1 && n100 !== 11) return forms.one;
  if (n10 >= 2 && n10 <= 4 && (n100 < 10 || n100 >= 20)) return forms.two;
  return forms.five;
}

export type TFunc = (key: Key, vars?: Record<string, string | number>) => string;

function makeT(lang: Lang): TFunc {
  const dict = DICTS[lang];
  return (key, vars) => {
    const raw = dict[key];
    if (typeof raw === "string") return interpolate(raw, vars);
    // объект плюрализации: ждём числовой vars.n
    const n = typeof vars?.n === "number" ? vars.n : 0;
    return interpolate(pickPlural(lang, n, raw), vars);
  };
}

// Кэш связанных t по языку — чтобы не пересоздавать функцию на каждый рендер.
const T_CACHE: Partial<Record<Lang, TFunc>> = {};
export function tFor(lang: Lang): TFunc {
  let fn = T_CACHE[lang];
  if (!fn) {
    fn = makeT(lang);
    T_CACHE[lang] = fn;
  }
  return fn;
}

// ── реактивный хук: t перестраивается при смене языка (lang живёт в Zustand) ─────
export function useT(): TFunc {
  const lang = useStore((s) => s.lang);
  return tFor(lang);
}

// ── не-реактивный доступ для кода вне React ──────────────────────────────────────
// Модули lib/* (форматтеры единиц, генераторы файлов, парсеры) и разовые действия
// (скачивание ключа/бэкапа) не могут вызвать хук. `tg` берёт текущий язык из стора
// «на месте». Если результат рендерит компонент — он и так перерисуется при смене
// языка, т.к. подписан на lang через useT; для скачиваемых файлов берётся язык на
// момент клика, что и нужно.
export function currentLang(): Lang {
  return useStore.getState().lang;
}
export const tg: TFunc = (key, vars) => tFor(currentLang())(key, vars);
